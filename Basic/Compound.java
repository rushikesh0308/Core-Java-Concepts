public class Compound
{
	public static void main(String[] args)
	{
		int a=10;
		a+=5;
		System.out.println("a:"+a);//15

		int b=10;
		b-=5;
		System.out.println("b:"+b);//5

		int c=10;
		c*=5;
		System.out.println("c:"+c);//50
		
		int d=10;
		d/=5;
		System.out.println("d:"+d);//2

	}
}